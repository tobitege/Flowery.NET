            var figure = new PathFigure
            {
                StartPoint = isLeft
                    ? new Point(10, 2)
                    : new Point(2, 2),
                IsClosed = false
            };

            if (isLeft)
            {
                figure.Segments!.Add(new LineSegment { Point = new Point(2, 6) });
                figure.Segments.Add(new LineSegment { Point = new Point(10, 10) });
            }
            else
            {
                figure.Segments!.Add(new LineSegment { Point = new Point(10, 6) });
                figure.Segments.Add(new LineSegment { Point = new Point(2, 10) });
            }

            geometry.Figures!.Add(figure);
            path.Data = geometry;

            return path;
        }

