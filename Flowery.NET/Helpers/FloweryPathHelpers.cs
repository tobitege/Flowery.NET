        public static Geometry CreateSunriseGeometry(double size = 16)
        {
            var scale = size / 16d;
            var group = new GeometryGroup();
            group.Children!.Add(CreateEllipseGeometry(8 * scale, 7 * scale, 4 * scale, 4 * scale));
            group.Children.Add(new RectangleGeometry
            {
                Rect = new Rect(2 * scale, 12 * scale, 12 * scale, 2 * scale)
            });

            return group;
        }

        public static Geometry CreateSunsetGeometry(double size = 16)
        {
            var scale = size / 16d;
            var group = new GeometryGroup();
            group.Children!.Add(CreateEllipseGeometry(8 * scale, 9 * scale, 4 * scale, 4 * scale));
            group.Children.Add(new RectangleGeometry
            {
                Rect = new Rect(2 * scale, 4 * scale, 12 * scale, 2 * scale)
            });

            return group;
        }

        public static Geometry ParsePathGeometry(string data)
        {
            try
            {
                return StreamGeometry.Parse(data);
            }
            catch
            {
                return CreateFallbackWeatherGeometry();
            }
        }

        /// <summary>
        /// Parses a path data string into a Geometry object.
        /// </summary>
        public static Geometry ParseGeometry(string pathData)
        {
            return ParsePathGeometry(pathData);
        }

        public static void TrySetPathData(Path? path, Func<Geometry> geometryFactory, Func<Geometry>? fallbackFactory = null)
        {
            if (path == null)
                return;

            try
            {
                path.Data = geometryFactory();
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine($"[FloweryPathHelpers] Failed to set primary path data: {ex.Message}");

                if (fallbackFactory == null)
                    return;

                try
                {
                    path.Data = fallbackFactory();
                }
                catch (Exception fallbackEx)
                {
                    System.Diagnostics.Debug.WriteLine($"[FloweryPathHelpers] Failed to set fallback path data: {fallbackEx.Message}");
                }
            }
        }

        public static Geometry CreateEllipseGeometry(Point center, double radiusX, double radiusY)
        {
            return new EllipseGeometry { Center = center, RadiusX = radiusX, RadiusY = radiusY };
        }

        public static Geometry CreateEllipseGeometry(double centerX, double centerY, double radiusX, double radiusY)
        {
            return CreateEllipseGeometry(new Point(centerX, centerY), radiusX, radiusY);
        }

        private static Geometry CreateFallbackWeatherGeometry()
        {
            return CreateEllipseGeometry(12, 12, 10, 10);
        }
